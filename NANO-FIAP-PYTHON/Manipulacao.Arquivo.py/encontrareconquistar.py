# Encontrar e Reconquistar encontrando a posição de um caractere em uma string
# Usando o método find() para localizar a posição da primeira ocorrência de um caractere        


texto = "Eu estou aprendendo Python na FIAP"
print(texto.find("o"))
# Saída: 4 (posição do primeiro 'o' na string)
print(texto.find("Python"))
# Saída: 18 (posição do 'P' em 'Python')
print(texto.find("z"))
# Saída: -1 (caractere não encontrado)  